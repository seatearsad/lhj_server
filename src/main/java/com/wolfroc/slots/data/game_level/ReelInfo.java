package com.wolfroc.slots.data.game_level;

public class ReelInfo {
	private int symId;
	private int num;
	private int type;
	public int getSymId() {
		return symId;
	}
	public void setSymId(int symId) {
		this.symId = symId;
	}
	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
	public int getType() {
		return type;
	}
	public void setType(int type) {
		this.type = type;
	}
}
